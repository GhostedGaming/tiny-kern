typedef struct {
    uint32_t col;
    uint32_t row;
    uint32_t max_cols;
    uint32_t max_rows;
    uint32_t origin_x;
    uint32_t origin_y;
} console_t;

static console_t console = {0, 0, 0, 0, 0, 0};

static void new_line(void) {
    console.col = 0;
    console.row++;

    if (console.row >= console.max_rows) {
        uint32_t stride = framebuffer_request.response->framebuffers[0]->pitch / 4;
        uint32_t *fb = framebuffer_request.response->framebuffers[0]->address;
        uint32_t row_bytes = stride * GLYPH_H;

        uint32_t *dst = &fb[console.origin_y * stride];
        uint32_t *src = &fb[(console.origin_y + GLYPH_H) * stride];
        uint32_t rows_to_move = (console.max_rows - 1) * GLYPH_H;

        memmove(dst, src, rows_to_move * stride * sizeof(uint32_t));

        uint32_t *last_row = &fb[(console.origin_y + (console.max_rows - 1) * GLYPH_H) * stride];
        memset(last_row, 0, row_bytes * sizeof(uint32_t));

        console.row = console.max_rows - 1;
    }
}

static void putchar(char c) {
    if (c == ' ') {
        uint32_t stride = framebuffer_request.response->framebuffers[0]->pitch / 4;
        uint32_t *fb = framebuffer_request.response->framebuffers[0]->address;
        uint16_t cell_w = GLYPH_W + LETTER_SPACING_PX;
        uint32_t glyph_x = console.origin_x + (console.col * cell_w);
        uint32_t glyph_y = console.origin_y + (console.row * GLYPH_H);

        for (uint8_t i = 0; i < GLYPH_H; i++) {
            uint32_t *fb_row = &fb[(glyph_y + i) * stride + glyph_x];
            for (uint8_t j = 0; j < cell_w; j++) {
                fb_row[j] = 0x00000000;
            }
        }

        console.col++;
        if (console.col >= console.max_cols)
            new_line();
        return;
    } else if (c == '\n') {
        new_line();
        return;
    } else if (c == '\r') {
        console.col = 0;
        return;
    } else if (c == '\b') {
        if (console.col > 0) {
            console.col--;
        } else if (console.row > 0) {
            console.row--;
            console.col = console.max_cols - 1;
        }

        uint32_t stride = framebuffer_request.response->framebuffers[0]->pitch / 4;
        uint32_t *fb = framebuffer_request.response->framebuffers[0]->address;
        uint16_t cell_w = GLYPH_W + LETTER_SPACING_PX;
        uint32_t glyph_x = console.origin_x + (console.col * cell_w);
        uint32_t glyph_y = console.origin_y + (console.row * GLYPH_H);

        for (uint8_t i = 0; i < GLYPH_H; i++) {
            uint32_t *fb_row = &fb[(glyph_y + i) * stride + glyph_x];
            for (uint8_t j = 0; j < cell_w; j++) {
                fb_row[j] = 0x00000000;
            }
        }

        return;
    }

    uint8_t ch = (uint8_t)c;
    if (ch >= 128) return;

    const uint8_t *glyph_rows = font[ch];

    uint32_t stride = framebuffer_request.response->framebuffers[0]->pitch / 4;
    uint32_t *fb = framebuffer_request.response->framebuffers[0]->address;
    uint16_t cell_w = GLYPH_W + LETTER_SPACING_PX;
    uint32_t glyph_x = console.origin_x + (console.col * cell_w);
    uint32_t glyph_y = console.origin_y + (console.row * GLYPH_H);

    for (uint8_t i = 0; i < GLYPH_H; i++) {
        uint8_t row_bits = glyph_rows[i];
        uint32_t *fb_row = &fb[(glyph_y + i) * stride + glyph_x];
        for (uint8_t j = 0; j < GLYPH_W; j++) {
            fb_row[j] = (row_bits & (1 << (7 - j))) ? 0xFFFFFFFF : 0x00000000;
        }
        for (uint8_t j = 0; j < LETTER_SPACING_PX; j++) {
            fb_row[GLYPH_W + j] = 0x00000000;
        }
    }

    console.col++;
    if (console.col >= console.max_cols)
        new_line();
}

void print(const char *fmt, ...) {
    va_list list;
    va_start(list, fmt);
    format(putchar, fmt, list);
    va_end(list);
}

void print_init(void) {
    struct limine_framebuffer *fb = framebuffer_request.response->framebuffers[0];
    console.max_cols = (uint32_t)(fb->width / (GLYPH_W + LETTER_SPACING_PX));
    console.max_rows = (uint32_t)(fb->height / GLYPH_H);
    console.col = 0;
    console.row = 0;
    console.origin_x = 0;
    console.origin_y = 0;
}